import { NextResponse } from 'next/server';

export const dynamic = 'force-static';

export async function GET() {
  const js = `(() => {
  const s = document.currentScript;
  const ds = (s && s.dataset) ? s.dataset : {};
  const org = ds.org || '';
  const agent = ds.agent || '';
  const version = ds.version || '';
  const theme = ds.theme || 'dark';
  const position = (ds.position || 'bottom-right');
  const openOnLoad = String(ds.open || 'false') === 'true';
  const debug = String(ds.debug || 'false') === 'true' || ds.debug === '1';

  const host = location.origin.replace(/\/$/, '');
  const params = new URLSearchParams();
  if (org) params.set('org', org);
  if (agent) params.set('agent', agent);
  if (version) params.set('v', version);
  if (theme) params.set('theme', theme);
  if (debug) params.set('debug', '1');
  const iframeUrl = host + '/public/embed/agent?' + params.toString();

  const root = document.createElement('div');
  root.id = 'lamdis-embed-root';
  root.style.position = 'fixed';
  root.style.zIndex = '2147483000';
  root.style[ position === 'bottom-left' ? 'left' : 'right' ] = '24px';
  root.style.bottom = '24px';
  root.style.display = 'flex';
  root.style.flexDirection = 'column';
  root.style.alignItems = position === 'bottom-left' ? 'flex-start' : 'flex-end';

  const btn = document.createElement('button');
  btn.type = 'button';
  btn.textContent = 'Chat';
  btn.setAttribute('aria-label', 'Open chat');
  btn.style.background = 'linear-gradient(135deg, #a21caf, #0284c7)';
  btn.style.color = 'white';
  btn.style.border = 'none';
  btn.style.borderRadius = '9999px';
  btn.style.boxShadow = '0 6px 20px rgba(2,132,199,0.4)';
  btn.style.padding = '10px 14px';
  btn.style.font = '600 14px system-ui, -apple-system, Segoe UI, Roboto, Ubuntu, Cantarell, Noto Sans, Helvetica Neue, Arial, sans-serif';
  btn.style.cursor = 'pointer';

  const panel = document.createElement('div');
  panel.style.marginTop = '10px';
  panel.style.width = 'min(420px, 92vw)';
  panel.style.height = 'min(70vh, 600px)';
  panel.style.borderRadius = '16px';
  panel.style.overflow = 'hidden';
  panel.style.border = '1px solid rgba(100,116,139,0.4)';
  panel.style.boxShadow = '0 16px 40px rgba(2,6,23,0.5)';
  panel.style.display = 'none';

  const iframe = document.createElement('iframe');
  iframe.src = iframeUrl;
  iframe.title = 'Lamdis Chat';
  iframe.style.width = '100%';
  iframe.style.height = '100%';
  iframe.style.border = '0';

  panel.appendChild(iframe);
  root.appendChild(btn);
  root.appendChild(panel);
  document.body.appendChild(root);

  const toggle = () => {
    const isOpen = panel.style.display !== 'none';
    panel.style.display = isOpen ? 'none' : 'block';
    btn.textContent = isOpen ? 'Chat' : 'Close';
  };
  btn.addEventListener('click', toggle);
  if (openOnLoad) toggle();
})();`;
  return new NextResponse(js, {
    headers: { 'content-type': 'application/javascript; charset=utf-8', 'cache-control': 'public, max-age=300' },
  });
}
